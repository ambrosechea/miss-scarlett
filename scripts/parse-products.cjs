// parse-products.cjs  — uses csv-parse to handle multi-line description fields
const fs   = require('fs')
const path = require('path')
const { parse } = require('csv-parse/sync')

const csvPath = 'C:/Users/tekni/Downloads/Miss Scarlett - Products.csv'
const raw = fs.readFileSync(csvPath, 'utf8')

const records = parse(raw, {
  columns: true,
  skip_empty_lines: true,
  relax_quotes: true,
  trim: true,
})

console.log('Total CSV rows:', records.length)

const COL = {
  handle     : 'Product Handle',
  name       : 'Product Name',
  desc       : 'Product Description',
  categories : 'Product Categories',
  mainImg    : 'Main Variant Image',
  moreImgs   : 'More Variant Images',
  price      : 'Variant Price',
}

// De-duplicate by handle — take first row for each product
const seen    = new Set()
const products = []

for (const r of records) {
  const handle = r[COL.handle]?.trim()
  if (!handle || seen.has(handle)) continue
  seen.add(handle)

  const categories = (r[COL.categories] || '').split(';').map(s => s.trim()).filter(Boolean)

  // Parse "More Variant Images" — semicolon-separated, may have spaces
  const moreRaw  = r[COL.moreImgs] || ''
  const moreImgs = moreRaw.split(';').map(s => s.trim()).filter(Boolean)

  // All images (main + extras), deduplicated
  const mainImg = (r[COL.mainImg] || '').trim()
  const allImgs = [...new Set([mainImg, ...moreImgs].filter(Boolean))]

  // Price — strip "$" and commas
  const priceRaw = r[COL.price] || ''
  const price    = priceRaw.replace(/[$,]/g, '').trim()

  products.push({
    handle,
    name       : r[COL.name]?.trim() || handle,
    description: r[COL.desc]?.trim() || '',
    categories,
    mainImg,
    allImgs,
    price,
  })
}

console.log('\nUnique products:', products.length)

// Group by primary collection (first category that isn't "ALL COLLECTIONS")
const collections = {}
for (const p of products) {
  const primary = p.categories.find(c => c !== 'ALL COLLECTIONS') || 'UNCATEGORISED'
  if (!collections[primary]) collections[primary] = []
  collections[primary].push(p)
}

console.log('\nBy collection:')
for (const [col, prods] of Object.entries(collections)) {
  console.log(`  ${col}: ${prods.map(p => p.handle).join(', ')}`)
}

// Write image URLs for download
const allUrls = []
for (const p of products) {
  for (const url of p.allImgs) {
    if (!allUrls.includes(url)) allUrls.push(url)
  }
}
console.log('\nTotal unique image URLs:', allUrls.length)
fs.writeFileSync('scripts/product-image-urls.txt', allUrls.join('\n'))
console.log('Saved product-image-urls.txt')

// Write SQL migration
let sql = `-- Migration: products schema + seed data
-- Generated by scripts/parse-products.cjs

CREATE TABLE IF NOT EXISTS products (
  id          INTEGER PRIMARY KEY AUTOINCREMENT,
  handle      TEXT    NOT NULL UNIQUE,
  name        TEXT    NOT NULL,
  description TEXT    NOT NULL DEFAULT '',
  price       TEXT    NOT NULL DEFAULT '',
  main_image  TEXT    NOT NULL DEFAULT '',
  active      INTEGER NOT NULL DEFAULT 1,
  created_at  DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS product_categories (
  id         INTEGER PRIMARY KEY AUTOINCREMENT,
  product_id INTEGER NOT NULL REFERENCES products(id) ON DELETE CASCADE,
  category   TEXT    NOT NULL
);

CREATE TABLE IF NOT EXISTS product_images (
  id         INTEGER PRIMARY KEY AUTOINCREMENT,
  product_id INTEGER NOT NULL REFERENCES products(id) ON DELETE CASCADE,
  url        TEXT    NOT NULL,
  sort_order INTEGER NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_product_categories_category   ON product_categories(category);
CREATE INDEX IF NOT EXISTS idx_product_categories_product_id ON product_categories(product_id);
CREATE INDEX IF NOT EXISTS idx_product_images_product_id     ON product_images(product_id);

-- Seed products
`

function esc(s) { return (s || '').replace(/'/g, "''") }

for (const p of products) {
  sql += `INSERT INTO products (handle, name, description, price, main_image) VALUES ('${esc(p.handle)}', '${esc(p.name)}', '${esc(p.description)}', '${esc(p.price)}', '${esc(p.mainImg)}');\n`
}

sql += '\n-- Seed product_categories\n'
for (const p of products) {
  for (const cat of p.categories) {
    sql += `INSERT INTO product_categories (product_id, category) SELECT id, '${esc(cat)}' FROM products WHERE handle='${esc(p.handle)}';\n`
  }
}

sql += '\n-- Seed product_images\n'
for (const p of products) {
  p.allImgs.forEach((url, i) => {
    sql += `INSERT INTO product_images (product_id, url, sort_order) SELECT id, '${esc(url)}', ${i} FROM products WHERE handle='${esc(p.handle)}';\n`
  })
}

const outSql = 'migrations/0005_products.sql'
fs.writeFileSync(outSql, sql)
console.log('\nWrote', outSql)

// Write JSON for front-end development (optional local reference)
fs.writeFileSync('scripts/products.json', JSON.stringify(products, null, 2))
console.log('Wrote scripts/products.json')
